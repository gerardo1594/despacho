    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        cliente.setDni(txtDNI.getText());
        cliente.setNombre(txtNombre.getText());
        cliente.setApellidos(txtApellidos.getText());
        
        Abogado abogado = new Abogado();
        if(abogadoContrario){
            abogado.getProcurador().setNombreDespacho(txtProcurador.getText());
        }
        abogado.setNombre(txtNombreAbogado.getText());
        abogado.setApellidos(txtApellidosAbogado.getText());
        //abogado.addCliente(new Cliente("05335464Q","Gerardo","Meiro Mendoza"));
        cliente.setAbogado(abogado);
        this.setVisible(false);
    }//GEN-LAST:event_btnGuardarActionPerformed

